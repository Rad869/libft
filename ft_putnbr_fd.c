#include "libft.h"

void	ft_putnbr_fd(int n, int fd)
{
	char	c;

	c = 0;
	if (n == -2147483648)
	{
		ft_putstr_fd("-2147483648", fd);
		return ;
	}
	if (n < 0)
	{
		write (fd, "-", 1);
		n = n * (-1);
	}
	if (n / 10)
		ft_putnbr_fd(n / 10, fd);
	c = '0' + (n % 10);
	ft_putchar_fd(c, fd);
}
